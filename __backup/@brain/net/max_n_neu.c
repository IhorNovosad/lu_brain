MaxNNeu max_n_neu_create(lu_size size)
{
	MaxNNeu self = (MaxNNeu) malloc(sizeof(struct max_n_neu));

	// Initialize
	self->list = arr_create(size);

	return self;
}

void max_n_neu_destroy(MaxNNeu* self)
{
	arr_destroy(&self->list);
	lu_free((void**) self);
}

